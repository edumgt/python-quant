# def 키워드로 함수를 정의한다.
# 함수명: corporate_tax
# 매개변수: taxable_income — 타입 힌트 int (정수)로 과세표준(원 단위)을 받는다.
# -> int : 반환값이 정수(세금액)임을 명시하는 반환 타입 힌트
def corporate_tax(taxable_income: int) -> int:
    """법인세 계산 (2024년 기준)"""
    # 법인세 누진세율 구간 테이블을 리스트(list)로 정의한다.
    # 각 원소는 튜플(tuple): (과세표준 상한, 세율, 누진공제액)
    # 숫자 리터럴 안의 _ 는 가독성을 위한 자릿수 구분자 (값에 영향 없음)
    # float('inf') : 양의 무한대 — 최고 구간의 상한을 "무제한"으로 표현
    brackets = [
        (200_000_000,       0.09,           0),   # ~2억:    세율 9%,  공제 0원
        (20_000_000_000,    0.19,  20_000_000),   # ~200억:  세율 19%, 공제 2,000만원
        (300_000_000_000,   0.21,  60_000_000),   # ~3,000억: 세율 21%, 공제 6,000만원
        (float('inf'),      0.24, 150_000_000),   # 3,000억 초과: 세율 24%, 공제 1.5억원
    ]

    # for 루프로 brackets 리스트를 순회한다.
    # "limit, rate, deduction" 은 튜플 언패킹(tuple unpacking):
    #   각 튜플의 세 원소를 변수 세 개에 동시에 꺼낸다.
    for limit, rate, deduction in brackets:
        # 과세표준이 현재 구간 상한 이하이면 해당 구간 세율을 적용한다.
        # 누진세 공식: 세금 = 과세표준 × 세율 - 누진공제
        if taxable_income <= limit:
            # int() : 곱셈·뺄셈 결과가 float일 수 있으므로 정수로 변환
            # return 으로 함수를 즉시 종료하고 계산된 세금액을 반환
            return int(taxable_income * rate - deduction)

    # 정상적으로 brackets 를 순회하면 위에서 반드시 return 되므로
    # 이 줄은 방어 코드(defensive code)로, 실제로는 실행되지 않는다.
    return 0


# 일반 함수 정의: 정수를 천단위 콤마 포맷의 문자열로 변환한다.
# 매개변수: n — 포맷할 정수
# -> str : 반환값이 문자열임을 명시하는 반환 타입 힌트
def fmt(n: int) -> str:
    # f-string: {n:>20,} 은 n을 오른쪽 정렬(>), 20자리 폭, 천단위 콤마(,) 형식으로 포맷
    # 문자열 끝에 " 원" 을 리터럴로 붙여 반환
    return f"{n:>20,} 원"


# if __name__ == "__main__": 관용구
# 이 스크립트를 직접 실행할 때만(python3 0102.py) 아래 블록을 수행한다.
# 다른 파일에서 import 할 경우에는 실행되지 않는다.
# __name__ : 현재 모듈의 이름을 담은 내장 변수;
#            직접 실행 시 "__main__", import 시 모듈 파일명이 된다.
if __name__ == "__main__":
    # 테스트할 (설명 레이블, 과세표준) 쌍을 리스트로 정의한다.
    # 각 원소는 2-튜플: (str 레이블, int 금액)
    cases = [
        ("중소기업 (1억)",          100_000_000),
        ("중소기업 (2억)",          200_000_000),
        ("중견기업 (50억)",       5_000_000_000),
        ("중견기업 (200억)",     20_000_000_000),
        ("대기업 (500억)",       50_000_000_000),
        ("대기업 (3000억)",     300_000_000_000),
        ("초대기업 (5000억)",   500_000_000_000),
    ]

    # 헤더 행 출력
    # {:<16} : 왼쪽 정렬(<), 16자리 폭으로 문자열을 출력
    # {:>22} : 오른쪽 정렬(>), 22자리 폭으로 문자열을 출력
    # {:>10} : 오른쪽 정렬(>), 10자리 폭으로 문자열을 출력
    print(f"{'구분':<16} {'과세표준':>22} {'법인세':>22} {'실효세율':>10}")

    # "-" * 74 : 문자열 반복 연산 — "-" 를 74번 반복한 구분선 생성
    print("-" * 74)

    # for 루프로 cases 리스트를 순회하며 각 사례를 계산·출력한다.
    # "label, income" 은 튜플 언패킹
    for label, income in cases:
        # corporate_tax 함수를 호출해 법인세를 계산한다.
        tax = corporate_tax(income)

        # 실효세율(%) 계산: 세금 ÷ 과세표준 × 100
        # 결과는 float(실수)
        rate = tax / income * 100

        # f-string으로 한 행을 포맷해 출력한다.
        # {label:<16}  : label을 왼쪽 정렬, 16자리 폭으로 출력
        # fmt(income)  : 앞서 정의한 fmt() 함수로 금액을 포맷한 문자열 삽입
        # fmt(tax)     : 세금도 동일하게 포맷
        # {rate:>9.2f} : rate를 오른쪽 정렬, 전체 9자리·소수점 2자리 실수로 출력
        print(f"{label:<16} {fmt(income)} {fmt(tax)} {rate:>9.2f}%")
