# def 키워드로 함수를 정의한다.
# 함수명: income_tax
# 매개변수: taxable_income — 타입 힌트 int (정수)로 과세표준(원 단위)을 받는다.
# -> int : 반환값이 정수(세금액)임을 명시하는 반환 타입 힌트
def income_tax(taxable_income: int) -> int:
    """종합소득세 계산 (2024년 기준, 지방소득세 제외)"""
    # 세율 구간(누진세) 테이블을 리스트(list)로 정의한다.
    # 각 원소는 튜플(tuple): (과세표준 상한, 세율, 누진공제액)
    # 숫자 리터럴 안의 _ 는 가독성을 위한 자릿수 구분자로, 값에는 영향 없음
    # float('inf') : 양의 무한대 — 최고 구간의 상한을 "무제한"으로 표현
    brackets = [
        (14_000_000,    0.06,          0),   # ~1,400만: 세율 6%,  공제 0원
        (50_000_000,    0.15,  1_260_000),   # ~5,000만: 세율 15%, 공제 126만원
        (88_000_000,    0.24,  5_760_000),   # ~8,800만: 세율 24%, 공제 576만원
        (150_000_000,   0.35, 15_440_000),   # ~1.5억:   세율 35%, 공제 1,544만원
        (300_000_000,   0.38, 19_940_000),   # ~3억:     세율 38%, 공제 1,994만원
        (500_000_000,   0.40, 25_940_000),   # ~5억:     세율 40%, 공제 2,594만원
        (1_000_000_000, 0.42, 35_940_000),   # ~10억:    세율 42%, 공제 3,594만원
        (float('inf'),  0.45, 65_940_000),   # 10억 초과: 세율 45%, 공제 6,594만원
    ]

    # for 루프로 brackets 리스트를 순회한다.
    # "limit, rate, deduction" 은 튜플 언패킹(tuple unpacking):
    #   각 튜플의 세 원소를 변수 세 개에 동시에 꺼낸다.
    for limit, rate, deduction in brackets:
        # 과세표준이 현재 구간 상한 이하이면 해당 구간 세율을 적용한다.
        # 누진세 공식: 세금 = 과세표준 × 세율 - 누진공제
        if taxable_income <= limit:
            # int() : 곱셈·뺄셈 결과가 float일 수 있으므로 정수로 변환
            # return 으로 함수를 즉시 종료하고 계산된 세금액을 반환
            return int(taxable_income * rate - deduction)

    # 정상적으로 brackets 를 순회하면 위에서 반드시 return 되므로
    # 이 줄은 방어 코드(defensive code)로, 실제로는 실행되지 않는다.
    return 0


# 함수 정의 밖 최상위(top-level) 코드: 스크립트 실행 시 바로 수행된다.
# for 루프로 소득 목록(리스트 리터럴)을 순회한다.
for income in [30_000_000, 80_000_000, 200_000_000, 500_000_000]:
    # 앞서 정의한 income_tax 함수를 호출해 세금액을 구한다.
    tax = income_tax(income)

    # f-string(포맷 문자열): f"..." 안에서 {변수:포맷} 형식으로 값을 삽입
    # {income:>12,} — income 값을 오른쪽 정렬(>), 12자리 폭, 천단위 콤마(,) 형식으로 출력
    # {tax:>11,}   — tax 값을 오른쪽 정렬(>), 11자리 폭, 천단위 콤마(,) 형식으로 출력
    # {tax/income*100:.1f} — 실효세율(%)을 소수점 1자리(.1f) 실수로 출력
    # print() : 결과 문자열을 표준 출력(콘솔)에 한 줄씩 출력
    print(f"소득 {income:>12,}원 → 세금 {tax:>11,}원 (실효세율 {tax/income*100:.1f}%)")
